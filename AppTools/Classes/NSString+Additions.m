- (NSMutableDictionary*)dictionaryForURL {
    NSMutableDictionary* params = [NSMutableDictionary dictionary];
    if ([self length] <= 0) {
        return params;
    }
    NSArray* keyvalues = [self componentsSeparatedByString:@"&"];
    [keyvalues enumerateObjectsUsingBlock:^(NSString* keyvalue, NSUInteger idx, BOOL *stop) {
        keyvalue = [keyvalue trim];
        if (keyvalue.length <= 0) {
            return;
        }
        NSRange range = [keyvalue rangeOfString:@"="];
        if (range.location == NSNotFound) {
            params[keyvalue] = @"";
        }
        else {
            NSString* key = [keyvalue substringToIndex:range.location];
            NSString* value = [keyvalue substringFromIndex:range.location + 1].decodeURL;
            params[key] = value;
        }
    }];
    return params;
}

