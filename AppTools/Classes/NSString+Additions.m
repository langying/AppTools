- (NSTextContainer*)containerWithFont:(UIFont*)font size:(CGSize)size color:(NSInteger)color line:(CGFloat)line paragraph:(CGFloat)paragraph indent:(CGFloat)indent {
    NSTextContainer* container = [[NSTextContainer alloc] initWithSize:size];
    NSTextStorage* storage = [[NSTextStorage alloc] initWithString:self];
    [storage addAttribute:NSFontAttributeName value:font range:NSMakeRange(0, self.length)];
    [storage addAttribute:NSForegroundColorAttributeName value:[UIColor RGBAColor:color] range:NSMakeRange(0, self.length)];
    [storage addAttribute:NSParagraphStyleAttributeName value:({
        NSMutableParagraphStyle* style = [[NSMutableParagraphStyle alloc] init];
        style.alignment           = NSTextAlignmentLeft;  // 对齐方式
        style.headIndent          = 0.0f;                 // 段落除了第一行的其他文字离边缘间距
        style.tailIndent          = 0.0f;                 // ???????
        style.lineSpacing         = line;                 // 行间距
        style.paragraphSpacing    = paragraph;            // 段间距
        style.minimumLineHeight   = font.pointSize;       // 最小行高
        style.maximumLineHeight   = font.pointSize * 2;   // 最大行高
        style.lineHeightMultiple  = 1.0f;                 // 可变行高,乘因数
        style.firstLineHeadIndent = indent;               // 段落首文字离边缘间距
        style;
    }) range:NSMakeRange(0, self.length)];
    [storage addLayoutManager:({
        NSLayoutManager* layout = [[NSLayoutManager alloc] init];
        [layout addTextContainer:container];
        layout;
    })];
    return container;
}


- (CGSize)textSizeWithFont:(UIFont *)font constrainedToSize:(CGSize)size{
    CGSize textSize;
    if (CGSizeEqualToSize(size, CGSizeZero)){
        NSDictionary *attributes = @{NSFontAttributeName:font};
        textSize = [self sizeWithAttributes:attributes];
    } else {
        NSStringDrawingOptions option = NSStringDrawingUsesLineFragmentOrigin | NSStringDrawingUsesFontLeading;
        NSDictionary *attributes = [NSDictionary dictionaryWithObject:font forKey:NSFontAttributeName];
        CGRect rect = [self boundingRectWithSize:size options:option attributes:attributes context:nil];
        textSize = rect.size;
    }
    return textSize;
}

